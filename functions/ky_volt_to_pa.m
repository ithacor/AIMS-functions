function [factor, phase_out, preamp_gain_out, hydro_sens_out] = ky_volt_to_pa ( f0 )
%
% factor [phase,preamp_gain,hydro_sens] = volt_to_pa ( f0 )
%       'f0': the center frequency in Hz 
%   'factor': the convertion facter in Pa/V
%
% The optional output variables are:
%       Phase: the fase change at f0 (degrees) (by the preamp)
% preamp_gain: the preamp gain at f0 (dB)
%  hydro_sens: the hydrophone sensitivity at f0 (dB rel 1V/muPa)
%
% Version 1.0, 2015-06-19, Currently assumes input frequency is 15 MHz.
% Version 1.1, 2015-06-22, Now accepts f0 as an input variable (1-20MHz supported).
% Version 1.2, 2015-06-24, f0 can now be a vector. 
%
% By Morten Fischer Rasmussen, 
%


if max(f0(:)) > 20e6, error('Error: ''f0'' larger than 20 MHz is not supported.'); end

% interpol_method = 'spline';
% interpol_method = 'cubic';
interpol_method = 'linear';

% Get preamp data
preamp1        = get_preamp_lower_gain;
preamp2        = get_preamp_upper_gain;
% Combine the two data sets
preamp_tot     = [preamp1; preamp2];
[~, unq_idx]   = unique(preamp_tot(:,1));
preamp_data    = preamp_tot(unq_idx, :);
% Give sensible names
preamp_f       = preamp_data(:,1) * 1e6;
preamp_gain_dB = preamp_data(:,2);
preamp_phase   = preamp_data(:,3);
preamp_c       = preamp_data(:,4);
% Get Hydrophone data
hydro_data     = get_hydrophone_calibration;
hydro_f        = hydro_data(:,1)*1e6;
hydro_sens_dB  = hydro_data(:,2);
hydro_c        = hydro_data(:,5);

% Amplifier gain at f0
gain_dB = interp1(preamp_f, preamp_gain_dB, f0);
% Amplifier capacitance at f0
c_a = interp1(preamp_f, preamp_c, f0, interpol_method);
% Hydrophone sensitivity at f0
sens_dB = interp1(hydro_f, hydro_sens_dB, f0, interpol_method); % dB rel 1V/muPa
                                                  %sens_dB = -265.18;   
% Hydrophone capacitance at f0
c_h = interp1(hydro_f, hydro_c, f0, interpol_method); % dB rel 1V/muPa




% Voltage division from capacitors
%c_c = 0e-12;    % 90deg coaxial plug
         %c_a = 6.1e-12;    % amplifier
         %c_h = 76.835e-12; % hydrophone

volt_div = c_h ./ (c_h + c_a);
volt_div_dB = 20*log10(volt_div);

combined_sense_dB = sens_dB + gain_dB + volt_div_dB; %dB rel 1V/muPa
combined_sense    = 10.^((combined_sense_dB+120)/20); % V/Pa
factor            = 1./combined_sense;                % Pa/V

% Old factor
%amin_factor_raw = 2.572;            %kPa/mW
%amin_factor = amin_factor_raw * 1e6 % Pa/V

%pressure = volt * amin_factor;


if nargout > 1, phase_out       = preamp_phase; end
if nargout > 2, preamp_gain_out = gain_dB;      end
if nargout > 3, hydro_sens_out  = sense_dB;     end

end







